/**
 * 业务域Controller
 *
 * @author DATAMASTER
 * @date 2026-02-12
 */
@Tag(name = "业务域")
@RestController
@RequestMapping("/cat/domain")
@Validated
public class CatalogDomainController extends BaseController {
    @Resource
    private ICatalogDomainService CatalogDomainService;

    @Operation(summary = "查询业务域列表")
    @GetMapping("/listpage")
    public CommonResult<PageResult<CatalogDomainRespVO>> listpage(CatalogDomainPageReqVO CatalogDomain) {
        PageResult<CatalogDomainDO> page = CatalogDomainService.getCatalogDomainPage(CatalogDomain);
        return CommonResult.success(BeanUtils.toBean(page, CatalogDomainRespVO.class));
    }

    @Operation(summary = "查询业务域列表")
    @GetMapping("/list")
    public CommonResult<List<CatalogDomainRespVO>> list(CatalogDomainPageReqVO CatalogDomain) {
        List<CatalogDomainDO> CatalogDomainList = CatalogDomainService.getCatalogDomainList(CatalogDomain);
        return CommonResult.success(BeanUtils.toBean(CatalogDomainList, CatalogDomainRespVO.class));
    }



    @Operation(summary = "导出业务域列表")
    @Log(title = "业务域", businessType = BusinessType.EXPORT)
    @PostMapping("/export")
    public void export(HttpServletResponse response, CatalogDomainPageReqVO exportReqVO) {
        exportReqVO.setPageSize(PageParam.PAGE_SIZE_NONE);
        List<CatalogDomainDO> list = (List<CatalogDomainDO>) CatalogDomainService.getCatalogDomainPage(exportReqVO).getRows();
        ExcelUtil<CatalogDomainRespVO> util = new ExcelUtil<>(CatalogDomainRespVO.class);
        util.exportExcel(response, CatalogDomainConvert.INSTANCE.convertToRespVOList(list), "应用管理数据");
    }

    @Operation(summary = "获取业务域详细信息")
    @GetMapping(value = "/{id}")
    public CommonResult<CatalogDomainRespVO> getInfo(@PathVariable("id") Long id) {
        CatalogDomainDO CatalogDomainDO = CatalogDomainService.getCatalogDomainById(id);
        return CommonResult.success(BeanUtils.toBean(CatalogDomainDO, CatalogDomainRespVO.class));
    }

    @Operation(summary = "新增业务域")
    @Log(title = "业务域", businessType = BusinessType.INSERT)
    @PostMapping
    public CommonResult<Long> add(@Valid @RequestBody CatalogDomainSaveReqVO CatalogDomain) {
        CatalogDomain.setCreatorId(getUserId());
        CatalogDomain.setCreateBy(getNickName());
        CatalogDomain.setCreateTime(DateUtil.date());
        return CommonResult.toAjax(CatalogDomainService.createCatalogDomain(CatalogDomain));
    }

    @Operation(summary = "修改业务域")
    @Log(title = "业务域", businessType = BusinessType.UPDATE)
    @PutMapping
    public CommonResult<Integer> edit(@Valid @RequestBody CatalogDomainSaveReqVO CatalogDomain) {
        CatalogDomain.setUpdatorId(getUserId());
        CatalogDomain.setUpdateBy(getNickName());
        CatalogDomain.setUpdateTime(DateUtil.date());
        return CommonResult.toAjax(CatalogDomainService.updateCatalogDomain(CatalogDomain));
    }

    @Operation(summary = "删除业务域")
    @Log(title = "业务域", businessType = BusinessType.DELETE)
    @DeleteMapping("/{ids}")
    public CommonResult<Integer> remove(@PathVariable Long[] ids) {
        return CommonResult.toAjax(CatalogDomainService.removeCatalogDomain(Arrays.asList(ids)));
    }

    /**
     * 批量删除检查,查询可删除数和不可删除数
     */
    @Operation(summary = "批量删除检查业务域")
    @GetMapping("/batchDeleteCheck/{ids}")
    public CommonResult<BatchDeleteCheck<Long>> batchDeleteCheck(@PathVariable Long[] ids) {
        BatchDeleteCheck<Long> result = CatalogDomainService.batchDeleteCheck(Arrays.asList(ids));
        return CommonResult.success(result);
    }
}










































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































